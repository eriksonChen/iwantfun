$(function(){   
    var len = 20; // 超過N個字以"..."取代
    $(".text-digest-20").each(function(i){
        if($(this).text().length>len){
            $(this).attr("title",$(this).text());
            var text=$(this).text().substring(0,len-1)+" ...";
            $(this).text(text);
        }
    });
});

